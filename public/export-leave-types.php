  </style>
</head>
<body>
  <div class="head">
    <?php if ($logoData): ?><img src="data:image/jpeg;base64,<?= $logoData ?>" alt="UPF"><?php endif; ?>
    <div>
      <div class="org"><?= e(APP_ORG) ?></div>
      <div class="sys"><?= e(APP_NAME) ?></div>
      <div class="motto"><?= e(APP_MOTTO) ?></div>
    </div>
  </div>

  <h2 style="margin:0">Leave Export — <?= e($label) ?></h2>
  <div class="meta">Generated by <?= e($user['full_name'] ?? 'user') ?> on <?= date('j F Y H:i') ?></div>

  <table>
    <thead>
      <tr>
        <th>Employee</th><th>Service No</th><th>Branch</th><th>Leave Type</th><th>Start</th><th>End</th><th>Destination</th><th>Reason</th><th>Status</th><th>Submitted By</th><th>Submitted At</th><th>Manager</th><th>HQ</th>
      </tr>
    </thead>
    <tbody>
      <?php if (!$requests): ?>
        <tr><td colspan="13" style="text-align:center;color:#64748b">No leave requests found.</td></tr>
      <?php else: foreach ($requests as $r): ?>
        <tr>
          <td><?= e($r['emp_name']) ?></td>
          <td><?= e($r['service_no']) ?></td>
          <td><?= e($r['branch_name']) ?></td>
          <td><?= e($r['leave_type']) ?></td>
          <td><?= e($r['start_date']) ?></td>
          <td><?= e($r['end_date']) ?></td>
          <td><?= e($r['destination'] ?? '') ?></td>
          <td><?= e($r['reason'] ?? '') ?></td>
          <td><?= e($r['status']) ?></td>
          <td><?= e($r['submitter'] ?? '') ?></td>
          <td><?= e($r['submitted_at']) ?></td>
          <td><?= e($r['mgr_name'] ?? '') ?></td>
          <td><?= e($r['adm_name'] ?? '') ?></td>
        </tr>
      <?php endforeach; endif; ?>
    </tbody>
  </table>

  <div class="no-print">
