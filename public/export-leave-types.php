<?php
require_once __DIR__ . '/../includes/auth.php';
require_once __DIR__ . '/../includes/helpers.php';

$user = require_login();
$pdo = db();

$type = $_GET['type'] ?? 'csv'; // csv | html
$leaveType = $_GET['leave_type'] ?? '';
$dateFrom = $_GET['from'] ?? '';
$dateTo = $_GET['to'] ?? '';

// Scope to the same visibility rules as leave-requests.php
$where = '1=1';
$params = [];
if (is_manager($user)) {
    $where .= ' AND lr.branch_id = ?';
    $params[] = $user['branch_id'];
} elseif (is_officer($user)) {
    $where .= ' AND lr.submitted_by = ?';
    $params[] = $user['id'];
}

if (!empty($leaveType)) {
    $where .= ' AND lr.leave_type = ?';
    $params[] = $leaveType;
}

if (!empty($dateFrom)) {
    $where .= ' AND lr.start_date >= ?';
    $params[] = $dateFrom;
}
if (!empty($dateTo)) {
    $where .= ' AND lr.end_date <= ?';
    $params[] = $dateTo;
}

$sql = "SELECT lr.*, e.full_name AS emp_name, e.service_no, b.name AS branch_name,
              s.full_name AS submitter, m.full_name AS mgr_name, a.full_name AS adm_name
        FROM leave_requests lr
        JOIN employees e ON e.id=lr.employee_id
        JOIN branches b ON b.id=lr.branch_id
        LEFT JOIN users s ON s.id=lr.submitted_by
        LEFT JOIN users m ON m.id=lr.manager_reviewed_by
        LEFT JOIN users a ON a.id=lr.admin_reviewed_by
        WHERE $where
        ORDER BY lr.submitted_at DESC";
$stmt = $pdo->prepare($sql);
$stmt->execute($params);
$requests = $stmt->fetchAll();

$label = !empty($leaveType) ? $leaveType : 'All';
$baseName = 'UPF_MDD_Leave_' . preg_replace('/[^A-Za-z0-9_-]+/', '_', (string)$label) . '_' . date('Y-m-d');

if ($type === 'csv') {
    header('Content-Type: text/csv; charset=utf-8');
    header('Content-Disposition: attachment; filename="' . $baseName . '.csv"');
    $out = fopen('php://output', 'w');

    fputcsv($out, [APP_ORG]);
    fputcsv($out, [APP_NAME]);
    fputcsv($out, ['Leave Export — ' . $label]);
    fputcsv($out, ['Generated by: ' . ($user['full_name'] ?? 'user') . ' on ' . date('Y-m-d H:i')]);
    fputcsv($out, []);

    fputcsv($out, ['Employee','Service No','Branch','Leave Type','Start Date','End Date','Destination','Reason','Status','Submitted By','Submitted At','Manager Review','Admin Review']);

    foreach ($requests as $r) {
        fputcsv($out, [
            $r['emp_name'],
            $r['service_no'],
            $r['branch_name'],
            $r['leave_type'],
            $r['start_date'],
            $r['end_date'],
            $r['destination'] ?? '',
            $r['reason'] ?? '',
            $r['status'],
            $r['submitter'] ?? '',
            $r['submitted_at'],
            $r['mgr_name'] ?? '',
            $r['adm_name'] ?? ''
        ]);
    }

    fclose($out);
    exit;
}

$logoPath = BASE_PATH . '/public/assets/logo.jpg';
$logoData = file_exists($logoPath) ? base64_encode(file_get_contents($logoPath)) : '';
